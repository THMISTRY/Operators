package d.operators;

public class Arithmetic {

    public static void main(String[] args){

        int a= 20;
        int b=50;
        System.out.println(a+b); // 70
        System.out.println(a-b); // -30
        System.out.println(a*b); //1000
        System.out.println(a/b); //0
        System.out.println(a%b); //20

    }
}
